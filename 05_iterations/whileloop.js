// ******************* while , do while loop *******************

let index = 0  
while (index<=10 ) {
    console.log(`the value of index is : ${index}`);
    
}

const myArray = ["batmen","superman","ironmen"]

let arr = 0
while (arr == myArray.length ) {
    console.log(`the arr is ${myArray[arr]}`);
    arr ++
}

// do while loop 

let score =  1

do {
    console.log(`score is :${score}`);
    score ++
} while (score <= 10);

